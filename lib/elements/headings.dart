part of domino.nodes.elements;

/// Creates a <h1> Element.
Element h1({
  /* List<Setter> | Setter */ set,
  Map<String, String> attrs,
  Map<String, String> styles,
  List<String> classes,
  /* List, Component, Node, BuildFn, ... */ dynamic content,
  Map<String, EventHandler> on,
  dynamic key,
}) =>
    new Element(
      'h1',
      set: set,
      attrs: attrs,
      styles: styles,
      classes: classes,
      content: content,
      events: on,
      key: key,
    );

